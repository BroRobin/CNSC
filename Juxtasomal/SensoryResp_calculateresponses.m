%% Filter out cells without 3 stimuli
resp_class=NaN([143,1]);
for ii=1:size(responses,1)
    if isnan(responses(ii,1))==0 && isnan(responses(ii,2))==0 && isnan(responses(ii,3))==0
        % search for no responders
        if responses(ii,1)==0 && responses(ii,2)==0 && responses(ii,3)==0
            resp_class(ii)=0;
        end
        % search for single modalities
        if responses(ii,1)>0 && responses(ii,2)==0 && responses(ii,3)==0
            resp_class(ii)=1;

        elseif responses(ii,1)==0 && responses(ii,2)>0 && responses(ii,3)==0
            resp_class(ii)=1;

        elseif responses(ii,1)>0 && responses(ii,2)==0 && responses(ii,3)>0
            resp_class(ii)=1;
        end

        % search for two modalities
        if responses(ii,1)>0 && responses(ii,2)>0 && responses(ii,3)==0
            resp_class(ii)=2;
        elseif responses(ii,1)>0 && responses(ii,2)==0 && responses(ii,3)>0
            resp_class(ii)=2;
        elseif responses(ii,1)==0 && responses(ii,2)>0 && responses(ii,3)>0
            resp_class(ii)=2;
        end

        % search for three modalities
        if responses(ii,1)>0 && responses(ii,2)>0 && responses(ii,3)>0
            resp_class(ii)=3;
        end
    end
end

